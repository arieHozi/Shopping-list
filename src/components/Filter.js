import React, { Component } from "react";
import { connect } from "react-redux";
import { filterProducts, sortProducts } from "../actions/productActions";

class Filter extends Component {
  render() {
    return !this.props.filterdProducts ? (
      <div class="loader">Loading...</div>
    ) : (
      <div className="filter">
        <div className="filter-result">
          {this.props.filterdProducts.length} Products
        </div>
        <div className="filter-sort">
          Order{" "}
          <select
            value={this.props.sort}
            onChange={(e) =>
              this.props.sortProducts(
                this.props.filterdProducts,
                e.target.value
              )
            }
          >
            <option value="latest">Latest</option>
            <option value="lowest">Lowest</option>
            <option value="highest">Highest</option>
          </select>
        </div>
        <div className="filter-size">
          Filter{" "}
          <select
            value={this.props.size}
            onChange={(e) =>
              this.props.filterProducts(this.props.products, e.target.value)
            }
          >
            <option value="">ALL</option>
            <option value="XS">XS</option>
            <option value="S">S</option>
            <option value="M">M</option>
            <option value="L">L</option>
            <option value="XL">XL</option>
            <option value="XXL">XXL</option>
          </select>
        </div>
      </div>
    );
  }
}

const mapStateToProps = (state) => ({
  size: state.products.size, //products -name that we gave on from combineReducer
  sort: state.products.sort,
  products: state.products.items,
  filterdProducts: state.products.filteredItems,
});

export default connect(mapStateToProps, { filterProducts, sortProducts })(
  Filter
);
